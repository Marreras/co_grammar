# Example 1
# Print count while is under 5
# count +=1 is a shorter way to  count = count + 1

count = 0
while count < 5:
    print(count)
    count += 1

# Example 2
# Counting down from 5

count = 5

while count > 0:
    print("the count is: " , count)
    count -= 1
    print("take off!!")

